   wire                     \`BUS_DELAY WLAST      ;
   wire                     \`BUS_DELAY WVALID     ;
   wire                     \`BUS_DELAY WREADY     ;
   \`ifdef AMBA_AXI_WUSER
   wire  [WIDTH_WUSER-1:0]  \`BUS_DELAY WUSER      ;
   \`endif
