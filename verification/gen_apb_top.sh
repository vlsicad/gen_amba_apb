   \`endif
   \`ifndef AMBA_AXI4
   wire  [WIDTH_SID-1:0]    \`BUS_DELAY WID        ; assign WID[WIDTH_SID-1:WIDTH_CID] = MST_ID;
   \`endif
   wire  [WIDTH_DA-1:0]     \`BUS_DELAY WDATA      ;
