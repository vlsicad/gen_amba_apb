       , .BREADY    ( BREADY       )
       , .ARID      ( ARID         [WIDTH_ID-1:0])
       , .ARADDR    ( ARADDR       )
       , .ARLEN     ( ARLEN        )
       , .ARLOCK    ( ARLOCK       )
       , .ARSIZE    ( ARSIZE       )
